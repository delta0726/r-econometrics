# 5.1 RCTの実行には時間とコストがかかる

# ＜ポイント＞
# - コストには｢現実的な費用 + 機会コスト｣の両方がかかる
# - RCTデータのように介入のないデータを分析用に作るのは現実的でない
#   --- 短期的に望ましくない行動を取らなければならない
#   --- それでも｢期間｣というバイアスが入る
# - 計量経済学や因果推論は｢理想的にはRCTをしたいが不可能｣な場合にRCTに近い検証を行えるツール



# 5.2 セレクションバイアスが起きる理由

